# Программа для вычисления площади прямоугольника
def calculate_rectangle_area():
    print("=== Вычисление площади прямоугольника ===")
    try:
        # Запрос длины прямоугольника
        length = float(input("Введите длину прямоугольника: "))
        # Запрос ширины прямоугольника
        width = float(input("Введите ширину прямоугольника: "))
        # Проверка, что длина и ширина положительные числа
        if length <= 0 or width <= 0:
            print("Ошибка: Длина и ширина должны быть положительными числами.")
            return
        # Вычисление площади
        area = length * width
        # Вывод результата
        print(f"Площадь прямоугольника: {area}")
    except ValueError:
        print("Ошибка: Введенные значения должны быть числами.")
